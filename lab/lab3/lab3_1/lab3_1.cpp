#include <iostream>
using namespace std;

int main()
{
    // 1. Создаём переменную
    int value = 42;
    
    // 2. Указатель на переменную
    int* ptr = &value;
    
    // 3. Адрес и значение
    cout << "Адрес указателя: " << ptr << endl;
    cout << "Значение по адресу: " << *ptr << endl;
    
    // 4. Изменяем значение через указатель
    *ptr = 100;
    
    // 5. Проверяем, что переменная изменилась
    cout << "Новое значение переменной: " << value << endl;
    
    // 6. Создаём массив
    int arr[] = {10, 20, 30, 40, 50};
    
    // 7. Выводим массив через указатель
    cout << "Массив через указатель: ";
    for (int* p = arr; p < arr + 5; p++) {
        cout << *p << " ";
    }
    cout << endl;
    
    // 8. Целая переменная
    int x = 5;
    int y = 9;
    
    // 9. Указатель с константным адресом
    int* const constPtr = &x;
    
    cout << "Через const указатель: " << *constPtr << endl;
    
    // Можно менять значение
    *constPtr = 10;
    cout << "После изменения: " << *constPtr << endl;
    cout << "Переменная x: " << x << endl;
    
    // Нельзя менять адрес
    // constPtr = &y;  // ОШИБКА компиляции!
    
    return 0;
}
